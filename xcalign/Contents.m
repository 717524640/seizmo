% Functions for Cross-Correlation Based Signal Alignment
%RESCALE_SNR    - Rescales SNR using an error function
%TTALIGN        - Solves for Relative/Absolute Travel Times via Weighted Least Sq
%TTNUMDEV       - Returns number of standard deviations of lags from solution
%TTPOLAR        - Solves for polarities given relative polarities
%TTREFINE       - Rearranges cross-correlation peak info
%TTRESID        - Returns the time residuals of lags from the solution
%TTSOLVE        - Solves relative arrival times & polarities
%TTSTDERR       - Weighted standard error of arrival times

