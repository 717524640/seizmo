% Ambient noise analysis functions
%ARRAY_STATION_NAMES       - Returns station names for an array
%AZISWEEP                  - Sliding azimuthal window record section of correlograms
%DAYDIRS_AMPSPECTRA        - Stacks amplitude spectra of day directories
%DAYDIRS_CORRELATE         - Correlates records in day directories
%DAYDIRS_MAKE              - Convert a directory of data to a dir/year/day/file system
%DAYDIRS_MERGECUT_25HRS    - Creates 25 hour records from a day directories
%DAYDIRS_NORMALIZE         - Temporal & spectral normalization of day dir records
%DAYDIRS_RESAMPLE          - Sample records in day directories to a new samplerate
%DAYDIRS_RINST             - Remove instrument response from records in day dirs
%DAYDIRS_ROTCORR           - Rotates correlograms in day directories
%DAYDIRS_STACKCORR         - Stacks correlograms in day directories
%MAKE_FULL_HORZ_VOLUMES    - Computes fk volumes for horizontal full stacks
%MAKE_FULL_Z_VOLUMES       - Computes fk volumes for vertical full stacks
%MAKE_MONTHLY_HORZ_VOLUMES - Computes fk volumes for horiz. monthly stacks
%MAKE_MONTHLY_Z_VOLUMES    - Computes fk volumes for vertical monthly stacks
