% Shortcut function names
%ADDF    - ADDRECORDS                     - Add SEIZMO records
%CH      - CHANGEHEADER                   - Change SEIZMO data header values
%CMPH    - COMPAREHEADER                  - List SEIZMO headers side-by-side for easy comparison
%CSTF    - CONVOLVE_SOURCE_TIMEFUNCTION   - Convolve source function on SEIZMO records
%DSTF    - DECONVOLVE_SOURCE_TIMEFUNCTION - Deconvolve source from SEIZMO records
%DIF     - DIFFERENTIATE                  - Differentiate SEIZMO records
%DIVO    - DIVIDEOMEGA                    - Integrate SEIZMO records in the frequency domain
%DIVF    - DIVIDERECORDS                  - Divide SEIZMO records
%GH      - GETHEADER                      - Get SEIZMO data header values
%GPOLY   - GETPOLYNOMIAL                  - Get polynomial fit to SEIZMO records
%JOINF   - JOINRECORDS                    - Join SEIZMO records into multiple-component record(s)
%LH      - LISTHEADER                     - List SEIZMO data headers
%MSTF    - MAKE_SOURCE_TIMEFUNCTION       - Returns source time functions for convolution
%MUL     - MULTIPLY                       - Multiply SEIZMO records by a constant
%MULO    - MULTIPLYOMEGA                  - Differentiate SEIZMO records in the frequency domain
%MULF    - MULTIPLYRECORDS                - Multiply SEIZMO records
%P0      - PLOT0                          - Plot SEIZMO data records in an evenly spaced record section
%P1      - PLOT1                          - Plot SEIZMO data records in individual subplots
%P2      - PLOT2                          - Overlay plot of SEIZMO data records
%PSPEC0  - PLOTSPECTRA0                   - Evenly spaced plot of SEIZMO data record spectra
%PSPEC1  - PLOTSPECTRA1                   - Plot SEIZMO data record spectra in individual subplots
%PSPEC2  - PLOTSPECTRA2                   - Overlay plot of SEIZMO data record spectra
%QH      - QUERYHEADER                    - List SEIZMO headers side-by-side for easy comparison
%QSNR    - QUICKSNR                       - Quick estimation of SNR for SEIZMO records
%RD      - READDATA                       - Read SEIZMO data from datafiles
%RDW     - READDATAWINDOW                 - Read data window into SEIZMO data structure
%RH      - READHEADER                     - Read header info from SEIZMO datafiles
%R       - READSEIZMO                     - Read datafiles into SEIZMO data structure
%RECSEC  - RECORDSECTION                  - Plots SEIZMO data records in a distance spaced record section
%RDEAD   - REMOVEDEADRECORDS              - Removes constant SEIZMO records
%RDUPES  - REMOVEDUPLICATES               - Remove duplicate SEIZMO records
%RMEAN   - REMOVEMEAN                     - Remove mean from SEIZMO records
%RPOLY   - REMOVEPOLYNOMIAL               - Remove polynomial trend from SEIZMO records
%RTR     - REMOVETREND                    - Remove linear trend from SEIZMO records
%REV     - REVERSE                        - Reverse SEIZMO records
%SPECSEC - SPECTRASECTION                 - Plots SEIZMO data record spectra in a record section
%DECI    - SQUISH                         - Downsample SEIZMO records by an integer factor
%SUB     - SUBTRACT                       - Subtract a constant from SEIZMO records
%SUBF    - SUBTRACTRECORDS                - Subtract SEIZMO records
%SYNC    - SYNCHRONIZE                    - Synchronizes the reference times of SEIZMO records
%SYNCSR  - SYNCRATES                      - Resample SEIZMO records to a common sample rate
%WH      - WRITEHEADER                    - Write SEIZMO data header info to datafiles
%W       - WRITESEIZMO                    - Write SEIZMO records to datafile

